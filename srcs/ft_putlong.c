#include "libft.h"

void		ft_putlong(long nb)
{
	if (nb < 0)
	{
		nb *= -1;
		ft_putchar('-');
	}
	if (nb > 9)
	{
		ft_putlong(nb /= 10);
		ft_putlong(nb %= 10);
	}
	else
		ft_putchar(nb + '0');
}