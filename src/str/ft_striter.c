#include "../../incl/str.h"

void		ft_striter(char *s, void (*f)(char *))
{
	size_t i;
	size_t z;

	i = 0;
	if (s && f)
	{
		z = ft_strlen(s);
		while (i < z)
		{
			(f)(s);
			s++;
			i++;
		}
	}
}
