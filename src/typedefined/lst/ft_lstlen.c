#include "../../../incl/lst.h"

size_t		ft_lstlen(t_list *lst)
{
	t_list	*tmp;
	size_t	i;

	i = 0;
	tmp = lst;
	if (tmp)
	{
		while (tmp)
		{
			tmp = tmp->next;
			i++;
		}
	}
	return (i);
}
