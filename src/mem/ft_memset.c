#include "../../incl/mem.h"

void		*ft_memset(void *b, int c, size_t len)
{
	u_int8_t *x;

	x = (u_int8_t *)b;
	while (len-- > 0)
	{
		*x = (u_int8_t)c;
		x++;
	}
	return (b);
}
