#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*p;

	p = lst;
	if (p && f)
	{
		while (p)
		{
			(*f)(p);
			p = p->next;
		}
	}
}
