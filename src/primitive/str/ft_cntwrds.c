#include "../../../incl/str.h"

size_t		ft_cntwrds(char *str, char c)
{
	size_t	i;
	char	*sc;

	i = 0;
	sc = str;
	if (str[i] != c)
		i++;
	while (*sc)
	{
		if ((*(sc - 1) == c || *(sc - 1) == 0) && *sc != c)
			i++;
		sc++;
	}
	return (i);
}
