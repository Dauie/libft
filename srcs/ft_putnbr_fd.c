#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
	}
	if (n > 9)
	{
		n = n / 10;
		n = n % 10;
		ft_putchar_fd(n + '0', fd);
	}
	else
		ft_putchar_fd(n + '0', fd); 
}
