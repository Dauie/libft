#include "libft.h"

int		ft_cnttrim(char const *s)
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s)
		{
			if (*s == ' ' || *s == '\t' || *s == '\n')
				i++;
		}
	}
	return (i);
}
