#include "../libft.h"

size_t		ft_itbllen(int **tbl)
{
	size_t	i;
	int		**tmp;

	i = 0;
	tmp = tbl;
	while (*tmp)
	{
		i++;
		tmp++;
	}
	return (i);
}
